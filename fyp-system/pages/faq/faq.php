                <h3>How do I submit my project deliverables?</h3>
                <div class="answer">
                    To submit your project deliverables:
                    <br><br>
                    1. Go to the "Projects" section in your dashboard
                    <br>
                    2. Click on "Submit Deliverable" button
                    <br>
                    3. Select the type of submission (proposal, progress report, final report)
                    <br>
                    4. Upload your documents in the required format (PDF preferred)
                    <br>
                    5. Fill in any required submission details
                    <br>
                    6. Click "Submit" to complete the submission
                    <br><br>
                    <strong>Note:</strong> Make sure to submit before the deadline and verify your submission was successful.
                </div>
